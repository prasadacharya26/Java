package com.wolken.exception_handling.list;

public class Festival {
	private String name;
	private String date;
	private byte noOfDays;
	private String specification;
	public String getName() {
		return name;
	}
	public String setName(String name) {
		return this.name = name;
	}
	public String getDate() {
		return date;
	}
	public String setDate(String date) {
		return this.date = date;
	}
	public byte getNoOfDays() {
		return noOfDays;
	}
	public byte setNoOfDays(byte noOfDays) {
		return this.noOfDays = noOfDays;
	}
	public String getSpecification() {
		return specification;
	}
	public String setSpecification(String specification) {
		return this.specification = specification;
	}
	
	
}
